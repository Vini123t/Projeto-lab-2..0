    public Object[][] getArray() {
        ArrayList<registro> ls = getRegistros();
        int length = ls.size();
        Object[][] s = new String[length][3];
        for (int i = 0; i < length; i++) {
            registro r = ls.get(i);
            for (int y = 0; y < 3; y++) {
                switch (y) {
                    case 0:
                        s[i][y] = "" + r.id;
                        break;
                    case 1:
                        s[i][y] = r.nome;
                        break;
                    case 2:
                        s[i][y] = r.email;
                        break;
                }
            }

        }

        return s;
    }

    public ArrayList getRegistros() {
        ArrayList<registro> ls = new ArrayList();
        Connection con = new ConnectionFactory().getConnection();
        String sql = "";
        if (respfin instanceof RespFinFisico) {
            sql = "SELECT Pessoa.id, Pessoa.nome,Pessoa.email "
                    + "FROM Pessoa JOIN Paciente ON "
                    + "    Pessoa.id = Paciente.id JOIN RespFinFisico ON "
                    + "        Paciente.respfin = RespFinFisico.id "
                    + "    WHERE RespFinFisico.id = '" + respfin.getidPessoa() + "'";
        } else if (respfin instanceof RespFinJuridico) {
            sql = "SELECT Pessoa.id, Pessoa.nome,Pessoa.email "
                    + "FROM Pessoa JOIN Paciente ON "
                    + "    Pessoa.id = Paciente.id JOIN RespFinJuridico ON "
                    + "        Paciente.respfin = RespFinJuridico.id "
                    + "    WHERE RespFinJuridico.id = '" + respfin.getidPessoa() + "'";
        }
        try {
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                registro r = new registro();
                r.id = rs.getInt("id");
                r.nome = rs.getString("nome");
                r.email = rs.getString("email");
                ls.add(r);
            }
            ps.close();
        } catch (SQLException e) {
            System.out.println("Erro " + e);
        }
        return ls;
    }

